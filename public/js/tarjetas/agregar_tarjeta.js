$(function(){

	$("#btn_crear_tarjeta").click(function(){
		$.ajax({
			url:"/crear_tarjeta",
			type:"POST",
			data:{
				cuenta_id:$("#cuenta_id").val(),
				numero_tarjeta:$("#numero_tarjeta").val(),
				nombre_tarjeta:$("#nombre_tarjeta").val(),
				saldo:$("#saldo_inicial").val(),
				descripcion:$("#descripcion").val(),
				saldo_bajo:$("#saldo_bajo").val(),
				divisa:$("#divisa").val(),
			},
			success:function(response){
				console.log(response);
				alert('Se ha creado la tarjeta con exito');
				$("#cuenta_id").val("")
				$("#numero_tarjeta").val("")
				$("#nombre_tarjeta").val("")
				$("#saldo_inicial").val("")
				$("#descripcion").val("")
				$("#saldo_bajo").val("")
				$("#divisa").val("")
			},
			error:function(error){
				console.log(error);
			}
		});
	});
})